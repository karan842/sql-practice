(SELECT WORKER_ID FROM WORKER
WHERE SALARY BETWEEN 50000 AND 100000);

/*
	23. WRITE AN SQL QUERY TO FETCH THE NO. OF WORKERS
    OF EACH DEPARTMENT IN THE DESCENDING ORDER
*/
